    public void inputData(){
         //mengambil model dari table yang ada
       DefaultTableModel ambilModel = (DefaultTableModel) TableData.getModel();
       int j = 1;
       for (int k = 0; k < ambilModel.getRowCount();k++){
           j+=1;
       }
       
       //masukkan data yang di input kedalam table
       ambilModel.addRow(new Object[]{
       j, TeksNama.getText(), TeksAlamat.getText(),jComboBox1.getSelectedItem(), TeksPass.getText() 
       });
       
       TeksNama.setText("");
       TeksPass.setText("");
       TeksAlamat.setText("");
       jComboBox1.setSelectedItem(null);
       
       int a = ambilModel.getRowCount();
       System.out.println(a);
       
    }

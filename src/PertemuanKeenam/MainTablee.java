    private void hapusAllActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusAllActionPerformed
        DefaultTableModel model = new DefaultTableModel();
        
        Object[] coloumsName = new Object[6];
        
        coloumsName[0] = "ID";
        coloumsName[1] = "Nama";
        coloumsName[2] = "Password";
        coloumsName[3] = "Alamat";
        coloumsName[4] = "Jenis Kelamin";
        coloumsName[5] = "Tanggal";
        
        model.setColumnIdentifiers(coloumsName);
        TableData.setModel(model);
        
        for(int l =0; l < model.getRowCount();l++){
            model.removeRow(l);
        }
        
    }//GEN-LAST:event_hapusAllActionPerformed

